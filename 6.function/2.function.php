<?php
//1.函数调用过程********************

function jisuan($num1,$num2,$oper){
	$res=0;//计算的结果.
	
switch($oper){
  case "+":
	$res=$num1+$num2;
	break;
  case "-":
	$res=$num1-$num2;
	break;
  case "*":
	$res=$num1*$num2;
	break;
  case "/":
	$res=$num1/$num2;
	break;
  default:
   echo "运算符错误！";
  	
}
  return $res;
  
}

$a=12;
$b=45;
$type="*";
$res=jisuan($a,$b,$type);
//这里调用函数的参数，是把变量的值传入函数中，而不是把变量传入
echo "res=".$res;
echo "<br/>";

//2.函数调用过程********************
/*
1.1.根据内存图，php代码执行只要一看到函数调用，php都会开辟一个新栈(空间)。<br/>
1.2.各栈间的变量是相互独立的。

*/
